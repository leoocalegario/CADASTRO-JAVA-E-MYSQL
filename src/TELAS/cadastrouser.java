    private void jbSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSalvarActionPerformed
        if (idcadfoto == 0) {
            Cadastro cadastro = new Cadastro();
            cadastro.setNomeCadastro(txnome.getText());
            cadastro.setCpf(jvcpf.getText());
            cadastro.setCep(jfCep.getText());
            cadastro.SetNomeSexo(cbsexo.getSelectedItem().toString());
            cadastro.SetNomeProfissao(cbprofissao.getSelectedItem().toString());
            cadastro.setFoto(txtB64.getText());

            CadastroDao2 cadastroDao2 = new CadastroDao2();
            cadastroDao2.inserir(cadastro);
            dispose();
        } else {

            try {
                Cadastro cadastro = new Cadastro();
                cadastro.setIdCadastro(idcadfoto);
                cadastro.setNomeCadastro(txnome.getText());
                cadastro.setCpf(jvcpf.getText());
                cadastro.setCep(jfCep.getText());
                cadastro.SetNomeSexo(cbsexo.getSelectedItem().toString());
                cadastro.SetNomeProfissao(cbprofissao.getSelectedItem().toString());
                cadastro.setFoto(txtB64.getText());

                CadastroDao2 cadastroDao2 = new CadastroDao2();
                cadastroDao2.alterar(cadastro);
                dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Ocorreu um erro inesperado:\n" + ex.getMessage(), "ERRO!", ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_jbSalvarActionPerformed

