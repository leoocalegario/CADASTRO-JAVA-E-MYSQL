    public void exportardados(Cadastro model) {
        SexualidadeDao sexualidadedao = new SexualidadeDao();
        atualizaSexualidade(sexualidadedao);
        ProfissaoDao profissaodao = new ProfissaoDao();
        atualizaProfissao(profissaodao);
        if (model.getIdCadastro() == 0) {
            idcadfoto = 0;
        } else {
            idcadfoto = model.getIdCadastro();
            jvId.setText(Integer.toString(model.getIdCadastro()));
            txnome.setText(model.getNomeCadastro());
            jvcpf.setText(model.getCpf());
            jfCep.setText(model.getCep());
            cbsexo.setSelectedItem(model.getNomeSexo());
            cbprofissao.setSelectedItem(model.getNomeProfissao());
        }
    }

